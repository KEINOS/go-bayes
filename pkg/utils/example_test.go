package utils_test

import (
	"fmt"
	"log"

	"github.com/KEINOS/go-bayes/pkg/utils"
)

// ----------------------------------------------------------------------------
//  AnyToUint64()
// ----------------------------------------------------------------------------

func ExampleAnyToUint64() {
	for i, v := range []any{
		int(1),
		int8(1),
		int16(1),
		int32(1),
		int64(1),
		uint(1),
		uint8(1), // uint8(x) == byte(x)
		byte(1),  // byte(x) == uint8(x)
		uint16(1),
		uint32(1),
		uint64(1),
		float32(1),
		float64(1),
		[]int8{1},
		[]uint8{1}, // []uint8{x} == []byte{x}
		[]byte{1},  // []byte{x} == []uint8{x}
		"1",
		[]string{"1"},
	} {
		uid, err := utils.AnyToUint64(v)
		if err != nil {
			log.Fatalf("item #%v error: %v", i, err)
		}

		fmt.Printf("%-10T --> %v\n", v, uid)
	}
	// Output:
	// int        --> 5499574118996709625
	// int8       --> 9267480926123474231
	// int16      --> 8302621575810894237
	// int32      --> 5099122415246212248
	// int64      --> 17661666406993562769
	// uint       --> 3336975496170789273
	// uint8      --> 17793169530411995228
	// uint8      --> 17793169530411995228
	// uint16     --> 3619354590694453937
	// uint32     --> 13669797085912375188
	// uint64     --> 4019400696499439099
	// float32    --> 14522299580392283628
	// float64    --> 2679853070506119653
	// []int8     --> 10376156057276158199
	// []uint8    --> 2782361327378389732
	// []uint8    --> 2782361327378389732
	// string     --> 12263243927642032000
	// []string   --> 16832052253739009849
}
